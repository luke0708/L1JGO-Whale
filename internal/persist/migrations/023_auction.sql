-- 血盟小屋基礎資料
CREATE TABLE IF NOT EXISTS houses (
    house_id    INT PRIMARY KEY,
    house_name  VARCHAR(64) NOT NULL DEFAULT '',
    house_area  INT NOT NULL DEFAULT 0,
    location    VARCHAR(64) NOT NULL DEFAULT ''
);

-- 拍賣佈告欄資料
CREATE TABLE IF NOT EXISTS auction_board (
    house_id     INT PRIMARY KEY REFERENCES houses(house_id),
    house_name   VARCHAR(64) NOT NULL DEFAULT '',
    house_area   INT NOT NULL DEFAULT 0,
    deadline     TIMESTAMPTZ NOT NULL DEFAULT NOW() + INTERVAL '30 days',
    price        BIGINT NOT NULL DEFAULT 100000,
    location     VARCHAR(64) NOT NULL DEFAULT '',
    old_owner    VARCHAR(45) NOT NULL DEFAULT '',
    old_owner_id INT NOT NULL DEFAULT 0,
    bidder       VARCHAR(45) NOT NULL DEFAULT '',
    bidder_id    INT NOT NULL DEFAULT 0
);

